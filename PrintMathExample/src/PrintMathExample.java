public class PrintMathExample {
    public static void main(String[] args) {
        System.out.println("H");
        System.out.println(5 / 2.0);
        System.out.println((2 + 5) * 2.0);
        System.out.println(5 % 3); //dzielenie z resztá


        boolean varBoolean = true;
        boolean varBoolean2 = false;

        System.out.println(varBoolean);
        System.out.println(varBoolean2);
        System.out.println(3 > 4);
        System.out.println(3 == 3);
        System.out.println(3 <= (2 + 1));
        System.out.println(3 >= (2 + 1));

        System.out.println(3 == 3 || 3 == 5);   // lub czyli warunek spelniony
        System.out.println(3 == 3 && 3 == 5);  // i
        System.out.println(3 != 3);  //rózne czyli jedno rozne od drugiego


    }
}
